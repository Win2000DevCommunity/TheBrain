#!/usr/bin/env python3
"""Generate large .conv training sets for TheBrain v13 easytrain."""
import os
import glob
import textwrap

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
OUT = os.path.join(ROOT, "data", "conv", "generated")
os.makedirs(OUT, exist_ok=True)


def write_conv(path, pairs):
    with open(path, "w", encoding="utf-8", newline="\n") as f:
        f.write("# Auto-generated by gen_conv_corpus.py\n")
        for u, a in pairs:
            u = " ".join(u.split())
            a = " ".join(a.split())
            if not u or not a:
                continue
            f.write(f"U: {u}\nA: {a}\n\n")


