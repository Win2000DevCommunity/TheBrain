def gen_from_c_sources():
    pairs = []
    for folder in ("safe", "dangerous"):
        pat = os.path.join(ROOT, "data", folder, "*.c")
        for path in glob.glob(pat):
            name = os.path.basename(path)
            try:
                with open(path, "r", encoding="utf-8", errors="replace") as f:
                    src = f.read(1200)
            except OSError:
                continue
            src = src.replace("\r\n", "\n").replace("\n", " ")
            src = " ".join(src.split())
            if len(src) < 40:
                continue
            pairs.append((
                f"Explain the C source file {name}",
                f"This file {name} contains C code. Key idea: {src[:400]}..."
            ))
            pairs.append((
                f"What does {name} demonstrate?",
                f"It demonstrates patterns in {name}. Summary: {src[:350]}..."
            ))
    write_conv(os.path.join(OUT, "code_from_c_files.conv"), pairs)


