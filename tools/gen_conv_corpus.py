def gen_malware_chat():
    pairs = []
    snippets = [
        ("What is ransomware?", "Ransomware encrypts victim files and demands payment for decryption keys."),
        ("What is a trojan?", "A trojan disguises itself as legitimate software while performing hidden malicious actions."),
        ("What is entropy in PE analysis?", "High entropy often indicates packing or encryption in a PE section."),
        ("What is VirtualAlloc used for?", "VirtualAlloc allocates memory. Malware may use it for shellcode or injection."),
        ("What is YARA?", "YARA matches patterns in files using rules to identify malware families."),
        ("How to analyze a suspicious EXE?", "Check PE headers, imports, sections, strings, entropy, and run in an isolated sandbox."),
    ]
    for q, a in snippets:
        pairs.append((q, a))
        for i in range(5):
            pairs.append((f"{q} (explain simply)", a))
    write_conv(os.path.join(OUT, "malware_chat_bulk.conv"), pairs)


